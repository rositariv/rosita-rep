package Lecture1.MyFirstPackage;

public class MyFirstClass {
    public static void main (String[] args){
        System.out.println("Rosi's first line of code!");
        int wholeNumber = 100;
        double fractionalNumber = 132.564;
        String text = "Rosi loves dogs!";
        String textKirilica = "Роси обича кучета!";

        System.out.println(wholeNumber);
        System.out.println(fractionalNumber);
        System.out.println(text);
        System.out.println(textKirilica);
        System.out.println("-------------");
        byte firstByte = 52;
        long firstLong = 1234L;
        float firstFloat = 12345.4512F;
        System.out.println(firstByte);
        System.out.println(firstLong);
        System.out.println(firstFloat);
        System.out.println("-------------");
        boolean firstBoolean = true;
        boolean secondBoolean = false;
        System.out.println(firstBoolean);
        System.out.println(secondBoolean);
        System.out.println("-------------");
        char singleLetter = 'a';
        String singleLetterString = "a";
        System.out.println(singleLetter);
        System.out.println(singleLetterString);
        System.out.println("-------------");

        short firstShort;
        firstShort = 6457;
        System.out.println(firstShort);
    }
}
